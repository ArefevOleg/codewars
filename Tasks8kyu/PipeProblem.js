// Задача
// Получив список уникальных чисел, отсортированных в порядке возрастания,
// верните новый список таким образом, чтобы значения увеличивались
// на 1 для каждого индекса от минимального значения до максимального (включая оба).
// Example
// Input:  1,3,5,6,7,8 Output: 1,2,3,4,5,6,7,8

function pipeFix(numbers){
  if (numbers.length === 0) {
    return [];
  }
  const min = Math.min(...numbers);
  const max = Math.max(...numbers);
  const resultArray = [];
  for (let i = min; i <= max; i++) {
    resultArray.push(i);
  }
  return resultArray;
}
